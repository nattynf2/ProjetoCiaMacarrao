package android.curso.projetociamacarrao.view.model;

public class Molhos {



    private int id_molho;
    private String nm_molho;
    private double vl_preco;
    private int qtd_molho;



public Molhos(){

}


    public int getId_molho() {
        return id_molho;
    }

    public void setId_molho(int id_molho) {
        this.id_molho = id_molho;
    }

    public String getNm_molho() {
        return nm_molho;
    }

    public void setNm_molho(String nm_molho) {
        this.nm_molho = nm_molho;
    }

    public double getVl_preco() {
        return vl_preco;
    }

    public void setVl_preco(double vl_preco) {
        this.vl_preco = vl_preco;
    }

    public int getQtd_molho() {
        return qtd_molho;
    }

    public void setQtd_molho(int qtd_molho) {
        this.qtd_molho = qtd_molho;
    }
}
